let currentLang = 'sq';

function translatePage(lang) {
    // FIX: update html lang attribute for screen readers & SEO
    document.documentElement.lang = lang;

    const elements = document.querySelectorAll('[data-key]');
    elements.forEach(el => {
        const key = el.getAttribute('data-key');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                // FIX: use textContent for plain text strings — safer than innerHTML
                el.textContent = translations[lang][key];
            }
        }
    });

    // Update all lang toggle buttons (desktop + mobile)
    const langBtns = document.querySelectorAll('#langToggle, #langToggleMobile');
    langBtns.forEach(btn => {
        if (lang === 'sq') {
            btn.textContent = '🇬🇧 English';
            btn.setAttribute('aria-label', 'Switch to English');
        } else {
            btn.textContent = '🇦🇱 Shqip';
            btn.setAttribute('aria-label', 'Kalo në Shqip');
        }
    });

    localStorage.setItem('blacknova_lang', lang);
}

// ==================== MODAL ====================
const modal = document.getElementById('contactModal');
const closeModalSpan = document.querySelector('.close-modal');
const closeModalBtn = document.querySelector('.close-modal-btn');
const contactButtons = document.querySelectorAll('.open-contact-btn');

// Track the element that opened the modal so we can return focus on close
let modalOpener = null;

function openModal(triggerEl) {
    if (!modal) return;
    modalOpener = triggerEl || document.activeElement;
    modal.style.display = 'flex';
    document.body.style.overflow = 'hidden';

    // FIX: move focus into modal for keyboard accessibility
    const firstFocusable = modal.querySelector('button, [href], input, [tabindex]:not([tabindex="-1"])');
    if (firstFocusable) firstFocusable.focus();
}

function closeModal() {
    if (!modal) return;
    modal.style.display = 'none';
    document.body.style.overflow = '';

    // FIX: return focus to the element that opened the modal
    if (modalOpener) modalOpener.focus();
}

// FIX: focus trap inside modal
modal.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') {
        closeModal();
        return;
    }
    if (e.key !== 'Tab') return;
    const focusable = modal.querySelectorAll('button, [href], input, [tabindex]:not([tabindex="-1"])');
    const first = focusable[0];
    const last = focusable[focusable.length - 1];
    if (e.shiftKey) {
        if (document.activeElement === first) { e.preventDefault(); last.focus(); }
    } else {
        if (document.activeElement === last) { e.preventDefault(); first.focus(); }
    }
});

if (contactButtons.length) {
    contactButtons.forEach(button => button.addEventListener('click', (e) => openModal(e.currentTarget)));
}
if (closeModalSpan) closeModalSpan.addEventListener('click', closeModal);
if (closeModalBtn) closeModalBtn.addEventListener('click', closeModal);

window.addEventListener('click', (e) => {
    if (e.target === modal) closeModal();
});

