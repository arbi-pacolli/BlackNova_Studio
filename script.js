// ==================== LANGUAGE TOGGLE ====================
const langToggle = document.getElementById('langToggle');
const langToggleMobile = document.getElementById('langToggleMobile');

function handleLangToggle() {
    currentLang = currentLang === 'sq' ? 'en' : 'sq';
    translatePage(currentLang);
}

if (langToggle) langToggle.addEventListener('click', handleLangToggle);
if (langToggleMobile) langToggleMobile.addEventListener('click', handleLangToggle);

// Restore saved language
const savedLang = localStorage.getItem('blacknova_lang');
if (savedLang === 'sq' || savedLang === 'en') {
    currentLang = savedLang;
}
translatePage(currentLang);

// ==================== SMOOTH SCROLL FOR NAV LINKS ====================
// CSS scroll-behavior: smooth handles anchor clicks, so we only add this
// for the mobile menu links to also close the menu on click
document.querySelectorAll('.nav-links a[href^="#"], .mobile-link[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        const targetId = this.getAttribute('href');
        const targetElement = document.querySelector(targetId);
        if (targetElement) {
            e.preventDefault();
            targetElement.scrollIntoView({ behavior: 'smooth' });
            // Close mobile menu if open
            closeMobileMenu();
        }
    });
});

// ==================== HAMBURGER / MOBILE MENU ====================
const hamburger = document.getElementById('hamburger');
const mobileMenu = document.getElementById('mobileMenu');

function openMobileMenu() {
    mobileMenu.classList.add('open');
    hamburger.classList.add('active');
    hamburger.setAttribute('aria-expanded', 'true');
    mobileMenu.setAttribute('aria-hidden', 'false');
}

function closeMobileMenu() {
    mobileMenu.classList.remove('open');
    hamburger.classList.remove('active');
    hamburger.setAttribute('aria-expanded', 'false');
    mobileMenu.setAttribute('aria-hidden', 'true');
}

if (hamburger) {
    hamburger.addEventListener('click', () => {
        if (mobileMenu.classList.contains('open')) {
            closeMobileMenu();
        } else {
            openMobileMenu();
        }
    });
}

// Close mobile menu when a contact button inside it is clicked
document.querySelectorAll('.mobile-menu .open-contact-btn').forEach(btn => {
    btn.addEventListener('click', closeMobileMenu);
});

// ==================== SCROLL REVEAL ANIMATIONS ====================
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            // FIX: apply stagger delay here, at animation time, not at load time
            const staggerIndex = entry.target.dataset.staggerIndex;
            if (staggerIndex !== undefined) {
                entry.target.style.animationDelay = `${staggerIndex * 0.1}s`;
            }
            entry.target.style.animation = 'fadeUp 0.6s ease forwards';
            observer.unobserve(entry.target);
        }
    });
}, observerOptions);

document.querySelectorAll('.section-header, .service-card, .why-card, .project-card, .process-step, .case-grid').forEach(el => {
    el.style.opacity = '0';
    observer.observe(el);
});

// FIX: store stagger index as data attribute so it's applied at animation time
const staggerElements = document.querySelectorAll('.service-card, .why-card, .project-card, .process-step');
staggerElements.forEach((el, index) => {
    el.dataset.staggerIndex = index;
});